package com.example.makeushifive.src.main.home.add;

public class DAYS {
    private String day;
    private String time;

    public DAYS(String day,String time){
        this.day=day;
        this.time=time;
    }

    public String getDay() {
        return day;
    }

    public String getTime() {
        return time;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
